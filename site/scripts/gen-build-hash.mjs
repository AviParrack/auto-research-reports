#!/usr/bin/env node
// Generates a unique build hash. Writes it to two places that must agree:
//   - src/lib/build-hash.ts      (imported by layouts → embedded in HTML)
//   - public/build-hash.txt      (served as-is with no-cache headers)
// The inline check script in the layout fetches build-hash.txt with
// cache: 'no-store' and compares to the embedded value. Mismatch → reload.

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');

const hash = new Date().toISOString().replace(/[:.]/g, '-');

fs.mkdirSync(path.join(root, 'public'), { recursive: true });
fs.writeFileSync(path.join(root, 'public', 'build-hash.txt'), hash);

fs.mkdirSync(path.join(root, 'src', 'lib'), { recursive: true });
fs.writeFileSync(
  path.join(root, 'src', 'lib', 'build-hash.ts'),
  `// AUTO-GENERATED by scripts/gen-build-hash.mjs — do not edit.\nexport const BUILD_HASH = ${JSON.stringify(hash)};\n`,
);

console.log(`[build-hash] ${hash}`);
